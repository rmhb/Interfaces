    private void listaEmpleadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_listaEmpleadosActionPerformed
        int selectedIndex = listaEmpleados.getSelectedIndex();
        nEmpleado.setText(trabajador.get(selectedIndex).getName());
        apeEmpleado.setText(trabajador.get(selectedIndex).getApellidos());       
        numEmpleado.setText( String.valueOf(trabajador.get(selectedIndex).getNumTrabajador()));
        
// Actualizar el tipo de trabajo de cada empleado... no rula... 
        jobs j = trabajador.get(selectedIndex).getJob();
        int index = 0;
        
        for(int i=0 ; i < curro.size() ; i++){
            if(curro.get(i).equals(j)){
                index = i;
                break;
            }
        }
        listaTrabajos.setSelectedIndex(index);        
    }//GEN-LAST:event_listaEmpleadosActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Boton de Guardar empleado que hemos editado
        if(nEmpleado.getText().isEmpty() || numEmpleado.getText().isEmpty() ||apeEmpleado.getText().isEmpty()) JOptionPane.showMessageDialog(null, "Rellena todos los campos");
        else{
            int selectedIndex = listaEmpleados.getSelectedIndex();
            trabajador.get(selectedIndex).setName(nEmpleado.getText().trim());
            trabajador.get(selectedIndex).setApellidos(apeEmpleado.getText().trim());
            trabajador.get(selectedIndex).setNumTrabajador(Integer.parseInt(numEmpleado.getText().trim()));
            jobs j = curro.get(listaTrabajos.getSelectedIndex());
            trabajador.get(selectedIndex).setJob(j);
            saveEmployeeToFile();
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
// Eliminar empleado
        int selectedIndex = listaEmpleados.getSelectedIndex();
        trabajador.remove(selectedIndex);
        saveEmployeeToFile();
    }//GEN-LAST:event_jButton1ActionPerformed

