    public void populateArrayList(){
        try{
            FileInputStream file = new FileInputStream("Jobs.dat");
            ObjectInputStream inputFile = new ObjectInputStream(file);
            boolean endOfFile=false;
            while(!endOfFile){
                try{
                    curro.add((jobs)inputFile.readObject());
                }catch(EOFException e){
                    endOfFile = true;
                }
                catch(Exception f){
                    JOptionPane.showMessageDialog(null, f.getMessage());
                }
                
            }
            inputFile.close();
        
        }catch(IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
            try{
            FileInputStream file2 = new FileInputStream("Empleados.dat");
            ObjectInputStream inputFile2 = new ObjectInputStream(file2);
            boolean endOfFile=false;
            while(!endOfFile){
                try{
                    trabajador.add((Empleado)inputFile2.readObject());
                }catch(EOFException e){
                    endOfFile = true;
                }
                catch(Exception f){
                    JOptionPane.showMessageDialog(null, f.getMessage());
                }               
            }
            inputFile2.close();
        
        }catch(IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    
    public void saveEmployeeToFile(){
        try{
            FileOutputStream file = new FileOutputStream("Empleados.dat");
            ObjectOutputStream outputFile = new ObjectOutputStream(file);
            
            for(int i=0 ; i < trabajador.size() ; i++ ){
                outputFile.writeObject(trabajador.get(i)); 
            }
            outputFile.close();
            JOptionPane.showMessageDialog(null, "Guardado Correctamente");
            this.dispose();
        }catch(IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

